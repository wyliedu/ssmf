public class CodeData {
	/**
	 * code
	 */
	private String code;
	/**
	 * 
	 * @param code
	 */
	public CodeData(String code) {
		super();
		this.code = code;
	}

	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}

	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	
	
}
